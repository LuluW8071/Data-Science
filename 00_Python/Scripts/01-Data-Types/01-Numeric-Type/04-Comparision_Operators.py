# Comparision Operators
''' >, <, >=, <=, ==, != '''

a = int(input('Enter the number that is greater than 10:\n'))

print(a > 10)
print(a < 10)
print(a >= 10)
print(a <= 10)
print(a == 10)
print(a != 10)

