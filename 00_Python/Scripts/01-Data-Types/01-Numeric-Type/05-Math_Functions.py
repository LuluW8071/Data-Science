import math 

x = -9.999
y = (1, 2, 3)

print(abs(x))           # returns absolute value (if -ve ---> +ve)
print(round(x))         # returns round off value
print(max(y))           # returns max value
print(min(y))           # returns min value
print(sum(y))           # returns sum 
print(pow(2, 3))        # returns power of x^y
print(math.sqrt(25))    # returns square root of a no
print(math.log(10))     # returns the natural logarithm of a no

# Output:
# 9.999
# -10
# 3
# 1
# 6
# 8
# 5.0
# 2.302585092994046