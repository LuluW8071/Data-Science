# User Input can be used using input() function
# Syntax
'''
variable = input()
'''

a = input('Enter your name:')
print('My name is', a)

'''
Note:
input() function takes everything as string
'''

x = input('Enter first number:')
y = input('Enter second number:')
print(x + y)

x = int(input('Enter first int number:'))
y = float(input('Enter second float number:'))
print('Result:', x * y)
