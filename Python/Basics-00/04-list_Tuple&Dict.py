# List is an ordered collection of data with elements separated by commas and enclosed within square brackets
# These lists can be modified aafter creation

list1 = [1, 2, 3, [-2, -3], ['a', 'b', 'c']]
print(list1)

# Tuple is an ordered collection of data with elements separated by commas and enclosed within paranthesis
# Tuples can't be modified aafter creation

tuple1 = (3, 4, 5, (-4, -5), ('x', 'y', 'z'))
print(tuple1)

# Dictionary(Mapped Data) is an unordered collection of data containing key:value pairs enclosed within curly brackets
dict1 = {'name': 'Python', 'age': 2023, 'language': True}
print(dict1)

# Output:
# [1, 2, 3, [-2, -3], ['a', 'b', 'c']]
# (3, 4, 5, (-4, -5), ('x', 'y', 'z'))
# {'name': 'Python', 'age': 2023, 'language': True}
