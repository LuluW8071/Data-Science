# Create a calculator capable of add, subtract, multiplicaiton, and division of any two numbers
'''Using only things that we learned from 01-07*.py'''

x = float(input('Enter your first number: '))
y = float(input('Enter your second number: '))

print('The value of', x, '+', y, 'is', x + y)
print('The value of', x, '-', y, 'is', x - y)
print('The value of', x, '*', y, 'is', x * y)
print('The value of', x, '/', y, 'is', x / y)
