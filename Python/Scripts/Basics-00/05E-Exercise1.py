# Create a calculator capable of add, subtract, multiplicaiton, and division of any two numbers
'''Using only things that we learned from 01-05*.py'''

a = 35
b = 12

print('The value of', a, '+', b, 'is', a + b)
print('The value of', a, '-', b, 'is', a - b)
print('The value of', a, '*', b, 'is', a * b)
print('The value of', a, '/', b, 'is', a / b)

# Output:
# The value of 35 + 12 is 47
# The value of 35 - 12 is 23
# The value of 35 * 12 is 420
# The value of 35 / 12 is 2.9166666666666665
